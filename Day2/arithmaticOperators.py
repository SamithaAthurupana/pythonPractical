#We can pass any numerical value into print function.
#llms helps to improve python writing performance

print(10*5)
print(10.+5) #Show floating value

#When multiply by float number show float number
print(2*2)
print(2*2.)
print(2.*2)
print(2.*2.)

#When divide by float number show float number
print(2 / 2)
print(2 / 2.)
print(2. / 2)
print(2. / 2.)

#When floor division
print(2 // 2)
print(2 // 2.)
print(2. // 2)
print(2. // 2.)

#Power
print(5 ** 5)
print(5 ** 5.)
print(5. ** 5)
print(5. ** 5.)

#Modules
print(2 % 2)
print(2 % 2.)
print(2. % 2)
print(2. % 2.)

print(2 + 6 / 3 * 3) #Normaly calculations are starting left side
print(2 ** 2 ** 2) #Power calculations are starting from right side